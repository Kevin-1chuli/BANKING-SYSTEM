    private void btnListAllHistoryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListAllHistoryActionPerformed

Map<String, Account> accounts = bankService.getAllAccounts();

    if (accounts.isEmpty()) {
        JOptionPane.showMessageDialog(this, "No accounts found.", "Accounts", JOptionPane.INFORMATION_MESSAGE);
        return;
    }

    StringBuilder sb = new StringBuilder();
    for (Account acc : accounts.values()) {
        sb.append(acc).append("\n");
    }

    JOptionPane.showMessageDialog(this, sb.toString(), "All Accounts", JOptionPane.INFORMATION_MESSAGE);

    }//GEN-LAST:event_btnListAllHistoryActionPerformed

